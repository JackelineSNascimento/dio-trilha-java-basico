public class OperadoresA3 {
    public static void main(String[] args) {
        int numero = 5;
        System.out.println(- numero);
        System.out.println(numero);
        
        numero = - numero;
        System.out.println(numero);
        System.out.println(-numero);

        numero = numero * -1; //não funcionou com "+ numero"
        System.out.println(numero);

    }
}
